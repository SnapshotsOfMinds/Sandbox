    /**
     * The algorithm to run.
     *
     * @param crossbar the current crossbar being checked
     */
    private void runAlgorithm(int[][] crossbar) {
        int internal = 0;
        int external = 0;
        int numHoles;

        // Loop over the 2d array two rows at a time
        for (int row = 0, row2 = 1; row < crossbar.length - 1; row++, row2++) {
            for (int col = 1; col < crossbar.length; col++) {
                //Checks for the patterns that match the following (external):
                // 11             01
                // 01             11
                if (crossbar[row][col] == 1 && crossbar[row2][col] == 1) {
                    if ((crossbar[row][col - 1] == 1 && crossbar[row2][col - 1] == 0) ||
                            (crossbar[row][col - 1] == 0 && crossbar[row2][col - 1] == 1)) {
                        external++;
                    }
                }
                //Checks for the patterns that match the following (external):
                // 10             11
                // 11             10
                if ((crossbar[row][col] == 0 && crossbar[row2][col] == 1) ||
                        (crossbar[row][col] == 1 && crossbar[row2][col] == 0)) {
                    if (crossbar[row][col - 1] == 1 && crossbar[row2][col - 1] == 1) {
                        external++;
                    }
                }
                //Checks for the patterns that match the following (internal):
                // 10             00
                // 00             10
                if (crossbar[row][col] == 0 && crossbar[row2][col] == 0) {
                    if ((crossbar[row][col - 1] == 0 && crossbar[row2][col - 1] == 1) ||
                            (crossbar[row][col - 1] == 1 && crossbar[row2][col - 1] == 0)) {
                        internal++;
                    }
                }
                //Checks for the patterns that match the following (internal):
                // 01             00
                // 00             01
                if ((crossbar[row][col] == 0 && crossbar[row2][col] == 1) ||
                        (crossbar[row][col] == 1 && crossbar[row2][col] == 0)) {
                    if (crossbar[row][col - 1] == 0 && crossbar[row2][col - 1] == 0) {
                        internal++;
                    }
                }
            }
        }

        // Calculates the number of holes in the crossbar
        numHoles = (external - internal) / 4;

        // Print out the 2d array
        for (int row = 0; row < crossbar.length; row++) {
            for (int col = 0; col < crossbar.length; col++) {
                System.out.print(crossbar[row][col]);
            }
            System.out.println();
        }

        // Print out the other requested information
        System.out.printf("The number of external corners is %d.\n", external);
        System.out.printf("The number of internal corners is %d.\n", internal);
        System.out.printf("The number of holes is %d.\n\n", numHoles);
    }
